                        <p><b>DO:</b>  Get seven to eight hours of sleep each night. That's the average amount most people need.
                            You may need more or less than that, but find what your body needs and aim to get it consistently.
                            Sleep helps the body repair itself, is necessary to consolidate memories and information,
                            can put you in a better mood and can help maintain your weight.</p>

                        <p>3. DON'T: Drink diet soda. Researchers who studied a large group of healthy,
                            postmenopausal women in the United States found that those who were "heavy consumers"
                            of diet drinks might be more prone to heart attacks, blood clots and other cardiovascular
                            problems. Other studies have linked diet sodas to abdominal obesity in adults over 65 and
                            an increase in sweet cravings.</p>
